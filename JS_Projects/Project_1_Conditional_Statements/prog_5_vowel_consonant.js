console.log("--- Program 5: Vowel or Consonant ---");
let char = "A";

if (
  char === "a" ||
  char === "A" ||
  char === "e" ||
  char === "E" ||
  char === "i" ||
  char === "I" ||
  char === "o" ||
  char === "O" ||
  char === "u" ||
  char === "U"
) {
  console.log(`${char} is a vowel.`);
} else {
  console.log(`${char} is a consonant.`);
}
console.log("--------------------------------------");

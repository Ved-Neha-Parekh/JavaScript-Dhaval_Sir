console.log("--- Program 3: Day of the Week ---");
let day = 4;

if (day === 1) {
  console.log(`Day ${day}: Monday`);
} else if (day === 2) {
  console.log(`Day ${day}: Tuesday`);
} else if (day === 3) {
  console.log(`Day ${day}: Wednesday`);
} else if (day === 4) {
  console.log(`Day ${day}: Thursday`);
} else if (day === 5) {
  console.log(`Day ${day}: Friday`);
} else if (day === 6) {
  console.log(`Day ${day}: Saturday`);
} else if (day === 7) {
  console.log(`Day ${day}: Sunday`);
} else {
  console.log(
    `Day ${day}: Invalid day number. Please a number between 1 and 7`
  );
}
console.log("------------------------------------");
//! Check if number is positive, negative, or zero.

console.log("--- 🔢 Program 1: Number Sign Check ---");
let num = 11; // Change this value to test: -2, 0, 14

if (num > 0) {
  console.log(`✅ ${num} is a positive number.`);
} else if (num < 0) {
  console.log(`❌ ${num} is a negative number.`);
} else {
  console.log(`➡️ ${num} is zero.`);
}

console.log("-------------------------------------");
